//PUSH

var ourArray = ["Stimpson", "J","cat"];
ourArray.push (["happy", "joy"]);
// ourArray now equals ["Stimpson", "J", "cat", ["happy","joy"]] sonuna ekledi.

//POP()

var ourArray= [1,2,3];
var removedFromOurArray = ourArray.pop();
// son elemanı kaldırdı [1,2]

//shıft()
var ourArray= [1,2,3];
var removedFromOurArray = ourArray.shift();
